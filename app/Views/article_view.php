                    <div id="articleTitle">
                        <h3>Flexural And Shear Behaviour Of Reinforced Concrete Slab With Pvc Pipe As A Cavity Forming In Two-Way System</h3>
                    </div>
                    <div id="authorString"><em>Cyntia Niani, Daniel Siahaan</em></div>
                    <br>
                    <div id="articleAbstract">
                        <h4>Abstract</h4>
                        <br>
                        <div style="font-size: 10pt;">Noise in requirements has been known as one of the quality-relatted issues of software requirements specification process. Thus, detecting defect at early stage is crutial in&nbsp; software development process. The noise could occur due to the existence of irrelevant information which is resided in the requirement statement. Previous study has tried to detect noise in requirements specification. It considers noise as outlier. Nevertheless, it could only demonstrate a moderate reliability because unique terms of actions overshadows unique terms of actors. This study propose MultiPhiLDA method for solving this issue. It modified LDA by distinguishes actors and actions as separate topic distribution models with two multinomial probability functions. This method maintain proportional weight contribution of actor and action. We also compare two approaches, Percentile and angle-based outlier detection (ABOD), &nbsp;for discriminating irrelevant requirements from the rest of the requirements. The experimental results shows that the proposed method improved the performance.</div>
